package task;

public class Task9A {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            if (i % 2 == 0) {
                System.out.println("Even");
            }
            else if  (i % 2 == 1) {
                System.out.println("Odd");
            }
        }

    }
}
